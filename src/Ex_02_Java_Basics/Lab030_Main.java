package Ex_02_Java_Basics;

public class Lab030_Main {
    public static void main(String[] args) {
        System.out.println(10+3/2-1*5);

        /*
        To solve the expression: 10 + 3 / 2 - 1 * 5

        ### Step 1: Follow the order of operations (PEMDAS/BODMAS)

        * **P/B**: Parentheses/Brackets
        * **E/O**: Exponents/Orders
        * **MD**: Multiplication and Division (from left to right)
        * **AS**: Addition and Subtraction (from left to right)

        ### Step 2: Solve multiplication and division first (left to right)

        Expression:
        `10 + 3 / 2 - 1 * 5`

        * `3 / 2 = 1.5`
        * `1 * 5 = 5`

        Now substitute back:
        `10 + 1.5 - 5`

        ### Step 3: Do addition and subtraction (left to right)

        * `10 + 1.5 = 11.5`
        * `11.5 - 5 = 6.5`

        ### Final Answer: **6.5**

         */

    }
}
